// shadcn/ui Toaster wrapper. We don't currently mount a <ThemeProvider>,
// so `useTheme()` returns undefined and falls back to the "light" default —
// matches DESIGN.md (light-only for now; the .dark block is wired but
// not active).

"use client";

import { useTheme } from "next-themes";
import { Toaster as Sonner } from "sonner";

type ToasterProps = React.ComponentProps<typeof Sonner>;

const Toaster = ({ ...props }: ToasterProps) => {
  const { theme } = useTheme();
  // exactOptionalPropertyTypes can't see through the default-binding, so
  // we narrow explicitly. Falls back to "light" when next-themes isn't
  // wired (no <ThemeProvider> mounted yet).
  const safeTheme: NonNullable<ToasterProps["theme"]> =
    theme === "dark" || theme === "light" || theme === "system"
      ? theme
      : "light";

  return (
    <Sonner
      theme={safeTheme}
      className="toaster group"
      toastOptions={{
        classNames: {
          toast:
            "group toast group-[.toaster]:bg-background group-[.toaster]:text-foreground group-[.toaster]:border-border group-[.toaster]:shadow-lg",
          description: "group-[.toast]:text-muted-foreground",
          actionButton:
            "group-[.toast]:bg-primary group-[.toast]:text-primary-foreground",
          cancelButton:
            "group-[.toast]:bg-muted group-[.toast]:text-muted-foreground",
        },
      }}
      {...props}
    />
  );
};

export { Toaster };
